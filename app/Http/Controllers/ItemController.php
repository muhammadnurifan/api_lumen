    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $this->validate($request,[
            'item_code' => 'required',
            'item_name' => 'required',
            'uom' => 'required'
        ]);

        $items = Item::create($request->all());
        return response()->json($items);
    }

